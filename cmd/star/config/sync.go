const generatedHeader = `# GENERATED BY star lint sync - DO NOT EDIT
# Source: star/config.yaml
# Regenerate: star lint sync
`

// SyncResult describes what was synced.
type SyncResult struct {
	GolangciLint    string // Path to generated .golangci.yaml
	MarkdownLint    string // Path to generated .markdownlint-cli2.yaml
	PrecommitConfig string // Path to generated .pre-commit-config.yaml
	FilesGenerated  int
}

// syncFromConfig generates tool-specific config files using ConfigAccessor paths.
func syncFromConfig(c *Config) (*SyncResult, error) {
	result := &SyncResult{}

	// Sync golangci-lint config if configured
	goConfig := c.Accessor("lint.go").Map("config")
	if len(goConfig) > 0 {
		path, err := syncGolangciLint(goConfig)
		if err != nil {
			return nil, fmt.Errorf("syncing golangci-lint config: %w", err)
		}
		result.GolangciLint = path
		result.FilesGenerated++
	}

	// Sync markdownlint config if configured
	mdConfig := c.Accessor("lint.markdown").Map("config")
	if len(mdConfig) > 0 {
		path, err := syncMarkdownLint(mdConfig)
		if err != nil {
			return nil, fmt.Errorf("syncing markdownlint config: %w", err)
		}
		result.MarkdownLint = path
		result.FilesGenerated++
	}

	// Sync pre-commit config if hooks are configured
	hooks := c.Accessor("precommit").Get("hooks")
	if hookSlice, ok := hooks.([]interface{}); ok && len(hookSlice) > 0 {
		path, err := syncPrecommitConfig(hookSlice)
		if err != nil {
			return nil, fmt.Errorf("syncing pre-commit config: %w", err)
		}
		result.PrecommitConfig = path
		result.FilesGenerated++
	}

	return result, nil
}

// syncGolangciLint writes .golangci.yaml from config values.
func syncGolangciLint(cfg map[string]interface{}) (string, error) {
	root := GitWorkspaceRoot()
	if root == "" {
		root = "."
	}
	path := filepath.Join(root, ".golangci.yaml")

	data, err := yaml.Marshal(cfg)
	if err != nil {
		return "", err
	}

	content := generatedHeader + string(data)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return "", err
	}

	return path, nil
}

// syncMarkdownLint writes .markdownlint-cli2.yaml from config values.
func syncMarkdownLint(cfg map[string]interface{}) (string, error) {
	root := GitWorkspaceRoot()
	if root == "" {
		root = "."
	}
	path := filepath.Join(root, ".markdownlint-cli2.yaml")

	// markdownlint-cli2 expects config under a "config" key
	wrapper := map[string]interface{}{
		"config": cfg,
	}

	data, err := yaml.Marshal(wrapper)
	if err != nil {
		return "", err
	}

	content := generatedHeader + string(data)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return "", err
	}

	return path, nil
}

// precommitRepo represents a repo in .pre-commit-config.yaml.
type precommitRepo struct {
	Repo  string          `yaml:"repo"`
	Hooks []precommitHook `yaml:"hooks"`
}

// precommitHook represents a hook in .pre-commit-config.yaml.
type precommitHook struct {
	ID            string   `yaml:"id"`
	Name          string   `yaml:"name"`
	Entry         string   `yaml:"entry"`
	Language      string   `yaml:"language"`
	PassFilenames bool     `yaml:"pass_filenames,omitempty"`
	Types         []string `yaml:"types,omitempty"`
	Stages        []string `yaml:"stages,omitempty"`
}

// precommitConfig represents the full .pre-commit-config.yaml structure.
type precommitConfig struct {
	Repos []precommitRepo `yaml:"repos"`
}

// syncPrecommitConfig writes .pre-commit-config.yaml from hook values.
func syncPrecommitConfig(hooks []interface{}) (string, error) {
	root := GitWorkspaceRoot()
	if root == "" {
		root = "."
	}
	path := filepath.Join(root, ".pre-commit-config.yaml")

	var pcHooks []precommitHook
	for _, h := range hooks {
		hMap, ok := h.(map[string]interface{})
		if !ok {
			continue
		}
		hook := precommitHook{
			ID:       mapString(hMap, "id"),
			Name:     mapString(hMap, "name"),
			Entry:    mapString(hMap, "entry"),
			Language: mapString(hMap, "language"),
		}
		if hook.Language == "" {
			hook.Language = "system"
		}
		if v, ok := hMap["pass_filenames"].(bool); ok {
			hook.PassFilenames = v
		}
		hook.Types = mapStringSlice(hMap, "types")
		hook.Stages = mapStringSlice(hMap, "stages")
		pcHooks = append(pcHooks, hook)
	}

	cfg := precommitConfig{
		Repos: []precommitRepo{
			{
				Repo:  "local",
				Hooks: pcHooks,
			},
		},
	}

	data, err := yaml.Marshal(cfg)
	if err != nil {
		return "", err
	}

	content := generatedHeader + string(data)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return "", err
	}

	return path, nil
}

