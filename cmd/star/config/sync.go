// Clean removes generated config files.
func Clean() error {
	root := GitWorkspaceRoot()
	if root == "" {
		root = "."
	}

	files := []string{
		filepath.Join(root, ".golangci.yaml"),
		filepath.Join(root, ".markdownlint-cli2.yaml"),
		filepath.Join(root, ".pre-commit-config.yaml"),
	}

	for _, f := range files {
		data, err := os.ReadFile(f)
		if os.IsNotExist(err) {
			continue
		}
		if err != nil {
			return err
		}

		if len(data) > len(generatedHeader) && string(data[:len(generatedHeader)]) == generatedHeader {
			if err := os.Remove(f); err != nil {
				return err
			}
		}
	}

	return nil
}

// EnsureGitignore ensures generated files are in .gitignore.
func EnsureGitignore() error {
	root := GitWorkspaceRoot()
	if root == "" {
		root = "."
	}

	gitignorePath := filepath.Join(root, ".gitignore")
	entries := []string{
		".golangci.yaml",
		".markdownlint-cli2.yaml",
		".pre-commit-config.yaml",
	}

	data, err := os.ReadFile(gitignorePath)
	if err != nil && !os.IsNotExist(err) {
		return err
	}

	content := string(data)
	var toAdd []string

	for _, entry := range entries {
		if !containsLine(content, entry) {
			toAdd = append(toAdd, entry)
		}
	}

	if len(toAdd) == 0 {
		return nil
	}

	f, err := os.OpenFile(gitignorePath, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0o644)
	if err != nil {
		return err
	}
	defer f.Close()

	if len(data) > 0 && data[len(data)-1] != '\n' {
		if _, err := f.WriteString("\n"); err != nil {
			return err
		}
	}
	if _, err := f.WriteString("\n# Generated by star lint sync\n"); err != nil {
		return err
	}
	for _, entry := range toAdd {
		if _, err := f.WriteString(entry + "\n"); err != nil {
			return err
		}
	}

	return nil
}

// containsLine checks if content contains a line matching entry.
func containsLine(content, entry string) bool {
	lines := filepath.SplitList(content)
	for _, line := range lines {
		if line == entry {
			return true
		}
	}
	return len(content) > 0 && (content == entry ||
		len(content) > len(entry) &&
			(content[:len(entry)+1] == entry+"\n" ||
				containsSubstring(content, "\n"+entry+"\n") ||
				(len(content) > len(entry) && content[len(content)-len(entry)-1:] == "\n"+entry)))
}

func containsSubstring(s, substr string) bool {
	return len(s) >= len(substr) && findSubstring(s, substr) >= 0
}

func findSubstring(s, substr string) int {
	for i := 0; i <= len(s)-len(substr); i++ {
		if s[i:i+len(substr)] == substr {
			return i
		}
	}
	return -1
}

// mapString extracts a string from a map.
func mapString(m map[string]interface{}, key string) string {
	if v, ok := m[key].(string); ok {
		return v
	}
	return ""
}

// mapStringSlice extracts a string slice from a map.
func mapStringSlice(m map[string]interface{}, key string) []string {
	v, ok := m[key]
	if !ok {
		return nil
	}
	switch s := v.(type) {
	case []string:
		return s
	case []interface{}:
		var result []string
		for _, item := range s {
			if str, ok := item.(string); ok {
				result = append(result, str)
			}
		}
		return result
	}
	return nil
}
